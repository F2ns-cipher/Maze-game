class Block {
  // Define constructor method with parameters
  constructor(initX, initY, initW, initH, initColor) {
    // Add properties to 'this' object
    this.x = initX;
    this.y = initY;
    this.w = initW;
    this.h = initH;
    this.color = initColor;
    console.log("New Block created.");
  }
}
